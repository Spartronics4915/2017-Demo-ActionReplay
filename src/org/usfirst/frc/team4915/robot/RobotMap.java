package org.usfirst.frc.team4915.robot;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {

    public static final int DRIVE_TRAIN_MOTOR_PORT_FOLLOWER = 1;
    public static final int DRIVE_TRAIN_MOTOR_PORT_MASTER = 3;

    public static final int DRIVE_TRAIN_MOTOR_STARBOARD_FOLLOWER = 2;
    public static final int DRIVE_TRAIN_MOTOR_STARBOARD_MASTER = 4;
}
